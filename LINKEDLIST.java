import java.util.*;
public class LINKEDLIST {
    public static void main(String args[])
    {
        LinkedList<Integer> a=new LinkedList<>();
        a.add(45);
        a.add(345);
        System.out.println(a);
        a.addFirst(9);
        System.out.println(a);
        a.addLast(34);
        System.out.println(a);
    }
    
}
